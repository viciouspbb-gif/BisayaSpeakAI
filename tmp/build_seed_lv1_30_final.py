def tokenize(text: str) -> list[str]:
    cleaned = re.sub(r"[?!,]", "", text)
    cleaned = cleaned.replace("-", " ")
    cleaned = cleaned.replace("'", " ")
    cleaned = re.sub(r"\s+", " ", cleaned).strip()
    return [token for token in cleaned.split(" ") if token]

